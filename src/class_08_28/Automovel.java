package class_08_28;

public class Automovel {

	private String modelo;
	private double preco;
	
	public void set_modelo(String modelo)
	{
		this.modelo = modelo;
	}
	
	public void set_preco(double preco)
	{
		this.preco = preco;
	}
	
	public String get_modelo()
	{
		return modelo;
	}
	
	public double get_preco()
	{
		return preco;
	}
}
